			świadczone w związku z wykonywaniem usług tłumaczeniowych,
			pośrednictwo w wykonywaniu tłumaczeń, doradztwo i konsultacje
			językowe itp.</li>

	</ol>
</div>
<div class="akapit">
	<h4>§ 3 Wykonywanie tłumaczeń pisemnych</h4>

	<ol>
		<li>1. Usługa tłumaczeniowa realizowana jest według obowiązującego
			cennika podstawowego.</li>
		<li>2. W zależności od wielkości zlecenia, terminu wykonania, stopnia
			trudności tekstu stawka za jedną stronę normatywną może być
			negocjowana z Klientem indywidualnie.</li>
		<li>3. Podstawową jednostką ilości tłumaczonego tekstu jest jedna
			strona normatywna. Jest to strona zawierająca 1800 znaków wraz ze
			spacjami (liczba ta jest obliczana w edytorze tekstu).</li>
		<li>4. Rzeczywista wartość zlecenia netto jest iloczynem ilości stron
			rozliczeniowych tekstu docelowego (gotowego tłumaczenia) oraz stawki
			wskazanej w cenniku podstawowym lub stawki ustalonej z Klientem w
			wyniku negocjacji.</li>
		<li>5. Termin realizacji zlecenia każdorazowo ustalany jest
			indywidualnie z Klientem.</li>
		<li>6. Tekst wykonanego tłumaczenia zostanie przesłany Klientowi w
			formie zapisu elektronicznego (w postaci transferu danych) lub w
			formie wydruku komputerowego, albo w obu tych formach jednocześnie, w
			zależności od indywidualnych ustaleń.</li>
		<li>7. Tekst tłumaczenia wysyłany jest do Klienta w formie
			elektronicznej (w formacie *.doc lub *.pdf). Na życzenie Klienta
			tłumaczenie może zostać wysłane faksem i/lub listem poleconym bądź
			pocztą kurierską za dodatkową odrębnie ustalaną dopłatą.</li>
		<li>8. W przypadku zleceń przesyłanych drogą elektroniczną, Klient
			zobowiązany jest do potwierdzenia otrzymania wykonanego tłumaczenia
			mailem, faksem lub telefonicznie w ciągu 48 godzin. W przypadku braku
			takiego potwierdzenia zlecenie uważa się za dostarczone i Klient nie
			ma podstaw do reklamacji z powodu nieterminowego dostarczenia
			tłumaczenia.</li>
		<li>9. Odtworzenie lub stworzenie elementów nietekstowych, takich jak
			wykresy, znaki graficzne, specjalne kroje i stopnie pisma, symbole i
			znaki spoza alfabetu polskiego i niemieckiego itp. będą przedmiotem
			indywidualnych ustaleń.</li>
		<li>10. Wszelkie wskazówki dotyczące sposobu tłumaczenia i
			wykorzystania w nim słownictwa, terminologii lub oznaczeń stosowanych
			wyłącznie przez Klienta winny być dostarczone w formie pisemnej wraz
			z tekstem do tłumaczenia. W przypadku braku wskazówek AdLing nie
			przyjmuje reklamacji dotyczących tłumaczenia nieuwzględniającego
			słownictwa, terminologii lub oznaczeń stosowanych przez Klienta.
			AdLing zastrzega sobie możliwość konsultacji z Klientem w zakresie
			słownictwa, terminologii lub oznaczeń specyficznych dla tekstów,
			będących przedmiotem zlecenia. AdLing nie ponosi odpowiedzialności za
			opóźnienia w realizacji zlecenia, powstałe w wyniku nieudzielenia
			bądź zwłoki w udzieleniu przez Klienta informacji w zakresie
			słownictwa, terminologii lub oznaczeń specyficznych dla tekstów,
			będących przedmiotem zlecenia.</li>
		<li>11. Klient ma prawo odwołać realizację zlecenia w dowolnym
			momencie z zastrzeżeniem, że jeśli Klient odwoła realizację zlecenia
			w trakcie jego wykonywania, wówczas będzie zobowiązany do zapłaty
			wynagrodzenia za pracę rzeczywiście wykonaną do momentu odwołania
			realizacji zlecenia oraz do zwrotu innych ewentualnych kosztów
			poniesionych przez AdLing związanych z przygotowaniem do świadczenia
			zamówionych usług. Odwołanie realizacji zlecenia wymaga formy
			pisemnej.</li>
		<li>12. AdLing zastrzega sobie prawo do odmowy wykonania usługi
			tłumaczeniowej bez podania przyczyny.</li>
		<li>13. Zlecenie musi zawierać dane Klienta niezbędne do wystawienia
			faktury VAT, w tym pełną nazwę, adres siedziby, NIP i ewentualny
			adres korespondencyjny.</li>
		<li>14. AdLing zastrzega sobie prawo do narzucenia Klientowi
			dodatkowych formalności i/lub czynności przed przystąpieniem do
			realizacji zlecenia, np. przesłania kopii dokumentów firmy (KRS,
			Regon, NIP), przesłania zlecenia faksem z podpisem i pieczęcią
			firmową, przesłania zlecenia z konta e-mail w domenie firmowej,
			przedpłaty, zapłaty przed realizacją zlecenia.</li>
	</ol>
