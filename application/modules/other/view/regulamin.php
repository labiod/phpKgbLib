<div class="akapit">
	<h4>§ 2 Rodzaj świadczonych usług</h4>

	<ol>
		<li>AdLing świadczy następujące usługi tłumaczeniowe i usługi
			dodatkowe:</li>
		<li>a) wykonanie tłumaczenia tekstu - to przygotowanie tekstu w języku
